<p><span class="glyphicon glyphicon-star-empty" aria-hidden="true"></span><b>  Geoinformatics Center (GIC) is a leading Remote Sensing and GIS organization in South East Asia.</b></p>

<p style=text-align:justify;>
Recognizing the emerging importance of spatial technologies and the potential future demand for skilled professionals, GIC was established in Asian Institute of Technology in 1995 (in the beginning GIC was called GIS Application Center - GAC) .  The aim was to create an environement that would help the need of the region in term of short-term training courses that are tailored to address potential applications in the regions. The initial financial support was provided by UNEP/RRC-AP. </p>
<p style=text-align:justify;>
In 1997, a joint collaboration of AIT with the <b>Asian Association of Remote Sensing (AARS)</b> saw the birth of its twin, the Asian Center for Research on Remote Sensing (ACRoRS) with a primary responsibility of carrying out Research and Consultancy works. Activities of this twin institutions or Center are synergistic and complementary. In the year 2004, these two centers were brought into one umbrella and named as Geoinformatics Center (GIC) of AIT. </p>
<p style=text-align:justify;>
Since the inception of the training center, more than 1,000 have been trained from the region with the support of various national and international agencies. GIC serves the region with appropriate and timely training courses specifically designed for the needs of participants. Dedicated staff of the center watchful of events in the region and track the interests of donor agencies in helping the region in capacity building activities in Geoinformatics.  Geoinformatics Center has undertaken a number of projects in <b>Bangladesh, Cambodia, Indonesia, Lao PDR, Nepal, Phillipines, Sri Lanka, Thailand and Vietnam</b>. It has carried out many training programs, workshops and conferences drawing participants from more than 25 countries within the Asia-Pacific Region.</p>
